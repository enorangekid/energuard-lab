/* 스코프에 맞는 평탄화된 키워드 목록 반환 */
function flattenKeywords(includeSubs) {
  const list = [];
  KW_TREE.forEach(g => {
    list.push({ keyword: g.main, mainKeyword: g.main, isSub: false });
    if (includeSubs) g.subs.forEach(s => list.push({ keyword: s, mainKeyword: g.main, isSub: true }));
  });
  return list;
}
